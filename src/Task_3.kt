fun main(args: Array<String>) {

    var five: Char = '5'
    var strFive: String = five.toString()
    var byteFive: Byte = five.toByte()
    var shortFive: Short = five.toShort()
    var intFive: Int = five.toInt()
    var doubleFive: Double = five.toDouble()
    var floatFive: Float = five.toFloat()
    var longFive: Long = five.toLong()

    println(strFive + "\n" + byteFive + "\n" + shortFive + "\n" + intFive + "\n" + doubleFive + "\n" + floatFive + "\n" + longFive)
}